interface CustomTooltipProps {
  active?: boolean;
  payload?: any[];
  label?: string;
}

const CustomTooltip: React.FC<CustomTooltipProps> = ({
  active,
  payload,
  label,
}) => {
  if (active && payload && payload.length) {
    return (
      <Card>
        <CardHeader>
          <CardTitle className="text-lg">{`${label} : ${formatter.format(
            payload[0].value
          )}`}</CardTitle>
        </CardHeader>
      </Card>
    );
  }

  return null;
};
